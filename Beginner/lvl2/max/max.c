int		max(int* tab, unsigned int len)
{
	int max = -2147483648;
	unsigned int i = 0;

	if (!tab || !tab[0])
		return (0);
	while (i < len)
	{
		if (tab[i] > max)
			max = tab[i];
		i++;
	}
	return (max);
}
