#include <unistd.h>

void pc(char c)
{
	write(1,&c,1);
}

int main(int c, char ** v)
{
	if (c != 2)
	{
		pc('\n');
		return -1;
	}
	int i = 0;
	while(v[1][i+1] != '\0')
		i++;
	while(v[1][i] == '\t' || v[1][i] == ' ')
		i--;
	if (i < 1)
	{
		pc('\n');
		return 1;
	}
	while(i>0 && v[1][i-1] != '\t' && v[1][i-1] != ' ')
		i--;
	while(v[1][i] && v[1][i] != '\t' && v[1][i] != ' ')
		pc(v[1][i++]);
	pc('\n');
	return 0;
}
