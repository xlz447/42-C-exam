#include <unistd.h>

int main(int c, char ** v)
{
	if (c != 2)
	{
		write(1, "\n", 1);
		return 0;
	}

	int print = 0;
	int i  = 0;

	while(v[1][i] != '\0')
	{
		if (v[1][i] != ' ' && v[1][i] != '\t')
		{
			if (print == 1)
			{
				write(1, " ", 1);
				print = 0;
			}
			write(1, &v[1][i], 1);
			if (v[1][i+1] == ' ' || v[1][i+1] == '\t')
				print = 1;
		}
		i++;
	}
	write(1, "\n", 1);
	return 0;
}
