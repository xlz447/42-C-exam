#include <unistd.h>

int main(int argc, char **argv)
{
	int i;
	char z;
	char nl;

	z = 'z';
	nl = '\n';
	i = 0;
	if (argc != 2)
	{
		write(1,&z,1);
		write(1,&nl,1);
		return (0);
	}
	while (argv[1][i] != '\0')
	{
		if (argv[1][i] == 'z')
		{
			write(1,&z,1);
			write(1,&nl,1);
			return (0);
		}
		i++;
	}
	write(1,&z,1);
	write(1,&nl,1);
	return (0);
}
