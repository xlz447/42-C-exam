#include <unistd.h>

void mypc(char c)
{
	write(1, &c, 1);
}

int main (int argc, char **argv)
{
	int i = 0;

	while (i < 26)
	{
		if(i % 2 == 0)
		{
			mypc('a' + i);
		}
		else
		{
			mypc('a' + i - 32);
		}
		i++;
	}
	mypc('\n');
	return 0;
	(void)argc;
	(void)argv;
}
